function RunTemplateWrapperRegression(charCaseName, charToolboxPath, charMexPath)
%% SIGNATURE
% RunTemplateWrapperRegression(charCaseName, charToolboxPath, charMexPath)
% -------------------------------------------------------------------------------------------------------------
%% DESCRIPTION
% MATLAB wrapper regression entry point for CTest.
% -------------------------------------------------------------------------------------------------------------
%% INPUT
% charCaseName   Test case name.
% charToolboxPath  Generated MATLAB class path.
% charMexPath    Generated MEX path.
% -------------------------------------------------------------------------------------------------------------
%% OUTPUT
% None.
% -------------------------------------------------------------------------------------------------------------
%% CHANGELOG
% May 2026  Pietro Califano     First prototype.
% -------------------------------------------------------------------------------------------------------------
%% DEPENDENCIES
% cpp_playground.CWrapperPlaceholder generated by gtwrap.
% -------------------------------------------------------------------------------------------------------------

arguments
    charCaseName (1, :) char
    charToolboxPath (1, :) char
    charMexPath (1, :) char
end

addpath(charToolboxPath);
addpath(charMexPath);

switch charCaseName
    case 'load_construct_clear'
        objWrapper = cpp_playground.CWrapperPlaceholder();
        clear objWrapper;
        clear classes;
        clear mex;

    case 'live_exit'
        objWrapper = cpp_playground.CWrapperPlaceholder();
        assignin('base', 'objWrapperLive', objWrapper);

    case 'string_value'
        objWrapper = cpp_playground.CWrapperPlaceholder();
        objWrapper.setTextDataByValue('value_ok');
        assert(strcmp(objWrapper.getTextData(), 'value_ok'));
        clear objWrapper;
        clear classes;
        clear mex;

    case 'string_const_ref'
        objWrapper = cpp_playground.CWrapperPlaceholder();
        objWrapper.setTextDataByConstRef('const_ref_ok');
        assert(strcmp(objWrapper.getTextData(), 'const_ref_ok'));
        clear objWrapper;
        clear classes;
        clear mex;

    case 'uint32_roundtrip'
        objWrapper = cpp_playground.CWrapperPlaceholder();
        ui32FrameId = objWrapper.echoFrameId(uint32(7));
        assert(isa(ui32FrameId, 'uint32'));
        assert(ui32FrameId == uint32(7));
        clear objWrapper;
        clear classes;
        clear mex;

    case 'bad_input_recovery'
        objWrapper = cpp_playground.CWrapperPlaceholder();
        ui32EchoWrapperId = GetWrapperMethodId_(charToolboxPath, 'echoFrameId');
        try
            template_project_wrapper(ui32EchoWrapperId, objWrapper, 'bad');
            error('RunTemplateWrapperRegression:ExpectedError', 'Bad direct MEX call unexpectedly passed.');
        catch objException
            if strcmp(objException.identifier, 'RunTemplateWrapperRegression:ExpectedError')
                rethrow(objException);
            end
        end
        ui32FrameId = objWrapper.echoFrameId(uint32(9));
        assert(ui32FrameId == uint32(9));
        clear objWrapper;
        clear classes;
        clear mex;

    case 'stdout_error_recovery'
        objWrapper = cpp_playground.CWrapperPlaceholder();
        objWrapper.printToStdout();
        ui32StringWrapperId = GetWrapperMethodId_(charToolboxPath, 'setTextDataByConstRef');
        try
            template_project_wrapper(ui32StringWrapperId, objWrapper);
            error('RunTemplateWrapperRegression:ExpectedError', 'Bad string MEX call unexpectedly passed.');
        catch objException
            if strcmp(objException.identifier, 'RunTemplateWrapperRegression:ExpectedError')
                rethrow(objException);
            end
        end
        objWrapper.printToStdout();
        clear objWrapper;
        clear classes;
        clear mex;

    case 'clear_all'
        evalin('base', 'objWrapper = cpp_playground.CWrapperPlaceholder(); clear all;');

    otherwise
        error('RunTemplateWrapperRegression:UnknownCase', 'Unknown MATLAB wrapper regression case: %s', charCaseName);
end

end

function ui32WrapperId = GetWrapperMethodId_(charToolboxPath, charMethodName)
arguments
    charToolboxPath (1, :) char
    charMethodName (1, :) char
end

charClassFile = fullfile(charToolboxPath, '+cpp_playground', 'CWrapperPlaceholder.m');
charClassText = fileread(charClassFile);
charPattern = [charMethodName, '[\s\S]*?template_project_wrapper\((\d+), this'];
cellTokens = regexp(charClassText, charPattern, 'tokens', 'once');
if isempty(cellTokens)
    error('RunTemplateWrapperRegression:MissingWrapperId', ...
          'Could not find wrapper id for method %s.', charMethodName);
end
ui32WrapperId = uint32(str2double(cellTokens{1}));
end
